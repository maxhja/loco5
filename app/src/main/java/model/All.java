package model;

public class All {

	private String id;
	private String names;

	public All() {

	}

	public All(String id, String names) {
		super();
		this.id = id;
		this.names = names;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNames() {
		return names;
	}

	public void setNames(String names) {
		this.names = names;
	}

	

}
